def slug_from_kw(kw):
    kw = (kw or '').strip()
    slug = re.sub(r'\s+', '-', kw)
    return re.sub(r'-+', '-', slug).strip('-')


data = json.loads(JSON_PATH.read_text(encoding='utf-8'))
for food in data['foods']:
    kw = food.get('rank_math_focus_keyword') or food.get('title', '')
    if kw not in COPY:
        continue
    patch = COPY[kw]
    food['intro'] = patch['intro']
    food['excerpt'] = patch['excerpt']
    food['rank_math_description'] = patch['rank_math_description']
    food['slug'] = slug_from_kw(kw)

JSON_PATH.write_text(json.dumps(data, ensure_ascii=False, indent=2) + '\n', encoding='utf-8')
print('updated', len(COPY), 'foods')
