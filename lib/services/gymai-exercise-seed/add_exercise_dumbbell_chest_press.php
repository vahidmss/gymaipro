function gymai_insert_dumbbell_chest_press_exercise() {
    if (!current_user_can('manage_options')) {
        return ['ok' => false, 'message' => 'دسترسی کافی ندارید.'];
    }

    $slug = 'پرس-سینه-با-دمبل';
    $existing = get_page_by_path($slug, OBJECT, GYMAI_EXERCISE_POST_TYPE);
    if ($existing instanceof WP_Post) {
        return [
            'ok' => false,
            'message' => 'این حرکت از قبل وجود دارد (ID: ' . $existing->ID . '). برای به‌روزرسانی از «بروزرسانی» استفاده کنید.',
            'post_id' => (int) $existing->ID,
        ];
    }

    $title = '۳ تمرین مهم پرس سینه با دمبل | افزایش حجم و قدرت سینه';
    $name_app = 'پرس سینه با دمبل';
    $content = gymai_add_exercise_dumbbell_chest_press_content();
    $image = 'https://gymaipro.ir/wp-content/uploads/2026/06/dumbbell-chest-press-flat.jpg';
    $thumb = 'https://gymaipro.ir/wp-content/uploads/2026/06/dumbbell-chest-press-flat-300x200.jpg';

    $post_id = wp_insert_post([
        'post_title' => $title,
        'post_name' => $slug,
        'post_type' => GYMAI_EXERCISE_POST_TYPE,
        'post_status' => 'publish',
        'post_content' => $content,
    ], true);

    if (is_wp_error($post_id)) {
        return ['ok' => false, 'message' => $post_id->get_error_message()];
    }

    gymai_apply_dumbbell_chest_press_meta((int) $post_id, $name_app, $content, $image, $thumb);

    return [
        'ok' => true,
        'message' => 'حرکت «پرس سینه با دمبل» با موفقیت ایجاد شد.',
        'post_id' => (int) $post_id,
    ];
}

/**
 * @return array{ok: bool, message: string, post_id?: int}
 */
function gymai_update_dumbbell_chest_press_exercise($post_id = 0) {
    if (!current_user_can('manage_options')) {
        return ['ok' => false, 'message' => 'دسترسی کافی ندارید.'];
    }

    if ($post_id <= 0) {
        $slug = 'پرس-سینه-با-دمبل';
        $existing = get_page_by_path($slug, OBJECT, GYMAI_EXERCISE_POST_TYPE);
        if (!$existing instanceof WP_Post) {
            return ['ok' => false, 'message' => 'پستی برای بروزرسانی پیدا نشد. ابتدا «افزودن» را بزنید.'];
        }
        $post_id = (int) $existing->ID;
    }

    $name_app = 'پرس سینه با دمبل';
    $content = gymai_add_exercise_dumbbell_chest_press_content();
    $image = 'https://gymaipro.ir/wp-content/uploads/2026/06/dumbbell-chest-press-flat.jpg';
    $thumb = 'https://gymaipro.ir/wp-content/uploads/2026/06/dumbbell-chest-press-flat-300x200.jpg';

    wp_update_post([
        'ID' => $post_id,
        'post_content' => $content,
        'post_title' => '۳ تمرین مهم پرس سینه با دمبل | افزایش حجم و قدرت سینه',
    ]);

    gymai_apply_dumbbell_chest_press_meta($post_id, $name_app, $content, $image, $thumb);

    return [
        'ok' => true,
        'message' => 'حرکت ID ' . $post_id . ' بروزرسانی شد (محتوا، متا v2، Rank Math).',
        'post_id' => $post_id,
    ];
