/// Result returned by the Coach v2 integration pipeline.
class CoachIntegrationResult {
  const CoachIntegrationResult({
    required this.intent,
    required this.coachContext,
    required this.decision,
    required this.responsePlan,
    required this.executorPreview,
    required this.processingTime,
    required this.missingProviders,
    required this.missingData,
    required this.confidence,
    required this.estimatedCost,
    required this.estimatedTokens,
    required this.estimatedLatency,
    required this.logs,
    this.promptPackage,
    this.conversationState,
    this.memoryApplication,
    this.pipelineTrace,
    this.skillExecutionResult,
    this.isLocalResponse = false,
    this.pipelineMode,
  });

  /// Builds a result from a successful local skill runtime execution.
  factory CoachIntegrationResult.local({
    required AIIntent intent,
    required CoachContext coachContext,
    required CoachSkillExecutionResult skillExecution,
    required Duration processingTime,
    required List<IntegrationEvent> logs,
    CoachConversationState? conversationState,
    EntityMemoryApplicationResult? memoryApplication,
    CoachPipelineTrace? pipelineTrace,
    CoachPipelineMode? pipelineMode,
  }) {
    final message = skillExecution.response.message ?? '';
    final decision = CoachDecision(
      shouldCallAI: false,
      localResponse: message,
      missingData: const <String>[],
      requiredProviders: const <AIContextProviderKey>{},
      missingProviders: const <AIContextProviderKey>{},
      decisionReason: const <CoachReason>{CoachReason.localAnswer},
      confidence: skillExecution.response.confidence,
      notes: <String>[
        'Local skill runtime response from ${skillExecution.skillId}.',
      ],
    );
    final responsePlan = CoachResponsePlan(
      id: 'skill_${skillExecution.skillId}',
      intent: intent,
      action: CoachAction.localResponse,
      requiresAI: false,
      requiredProviders: const <AIContextProviderKey>{},
      missingProviders: const <AIContextProviderKey>{},
      followUpQuestions: const <String>[],
      localMessage: message,
      contextKeys: const <AIContextProviderKey>{},
      confidence: skillExecution.response.confidence,
      estimatedTokens: 0,
      estimatedCost: 0,
      estimatedLatency: skillExecution.executionTime,
      notes: const <String>['Generated by local skill runtime.'],
      steps: const <ResponseStep>[
        ResponseStep(
          id: 'skill_local_response',
          action: CoachAction.localResponse,
          priority: ResponsePriority.high,
          description: 'Prepared LOCAL_RESPONSE route.',
        ),
      ],
    );
    final executorPreview = CoachExecutionPreview(
      plan: responsePlan,
      target: CoachExecutionTarget.local,
      wouldExecute: false,
      description:
          'Local skill ${skillExecution.skillId} response ready.',
    );

    return CoachIntegrationResult(
      intent: intent,
      coachContext: coachContext,
      decision: decision,
      responsePlan: responsePlan,
      executorPreview: executorPreview,
      processingTime: processingTime,
      missingProviders: const <AIContextProviderKey>{},
      missingData: const <String>[],
      confidence: skillExecution.response.confidence,
      estimatedCost: 0,
      estimatedTokens: 0,
      estimatedLatency: skillExecution.executionTime,
      logs: logs,
      conversationState: conversationState,
      memoryApplication: memoryApplication,
      pipelineTrace: pipelineTrace,
      skillExecutionResult: skillExecution,
      isLocalResponse: true,
      pipelineMode: pipelineMode,
    );
  }

  /// Detected intent.
  final AIIntent intent;

  /// Unified coach context package.
  final CoachContext coachContext;

  /// Coach decision produced before planning.
  final CoachDecision decision;

  /// Response plan generated from the decision.
  final CoachResponsePlan responsePlan;

  /// Structured prompt package for Coach v2 rendering.
  final PromptPackage? promptPackage;

  /// Conversation state snapshot after state-engine integration.
  final CoachConversationState? conversationState;

  /// Memory write result for this turn (auto + pending confirmations).
  final EntityMemoryApplicationResult? memoryApplication;

  /// Dry-run execution preview.
  final CoachExecutionPreview executorPreview;

  /// Total processing time for the pipeline.
  final Duration processingTime;

  /// Required providers that were missing.
  final Set<AIContextProviderKey> missingProviders;

  /// Required data fields that were missing.
  final List<String> missingData;

  /// Decision/plan confidence.
  final double confidence;

  /// Estimated relative cost.
  final double estimatedCost;

  /// Estimated token budget.
  final int estimatedTokens;

  /// Estimated latency.
  final Duration estimatedLatency;

  /// In-memory pipeline logs.
  final List<IntegrationEvent> logs;

  /// Unified pipeline trace when the orchestrator path is used.
  final CoachPipelineTrace? pipelineTrace;

  /// Local skill runtime execution result when a skill answered locally.
  final CoachSkillExecutionResult? skillExecutionResult;

  /// Whether the integration result came from local skill runtime.
  final bool isLocalResponse;

  /// Pipeline execution mode when the orchestrator path was used.
  final CoachPipelineMode? pipelineMode;
}
